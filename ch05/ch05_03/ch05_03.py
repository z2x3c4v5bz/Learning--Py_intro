def cc(num):

    num = int(num)

    if num < 1:
        num = 1

    if num > 25:
        num = 25

    a = [chr(i) for i in range(97, 122+1)] + [chr(j) for j in range(65, 90+1)]
    b = [a[(i+num)%26] for i in range(26)] + [a[(j+num)%26].upper() for j in range(26)]

    return dict(zip(a, b))


if __name__ == '__main__':

    d = cc(13)

    print(d)


